def unique_counter(object_list: list) -> int:
    res = []
    for obj in object_list:
        if obj not in res:
            res.append(obj)
    
    return len(res)


# 1. Простые числа (все уникальны)
test1 = [1, 2, 3, 4, 5]  
# Ожидаемый результат: 5

# 2. Повторяющиеся числа
test2 = [1, 2, 2, 3, 3, 3]  
# Ожидаемый результат: 3

# 3. Строки (регистр имеет значение)
test3 = ["apple", "Apple", "APPLE", "banana"]  
# Ожидаемый результат: 4

# 4. Смешанные типы (числа, строки, булевы значения)
test4 = [1, "1", True, False, 1.0]  
# Ожидаемый результат: 5 (1 != "1", True == 1, False == 0, но это зависит от реализации)

# 5. Пустой список
test5 = []  
# Ожидаемый результат: 0

# 6. Списки и кортежи (изменяемые vs неизменяемые)
test6 = [[1, 2], [1, 2], (1, 2), (1, 2)]  
# Ожидаемый результат: 2 (два уникальных объекта: один список и один кортеж)

# 7. Словари (изменяемые, нельзя хешировать)
test7 = [{"a": 1}, {"a": 1}, {"b": 2}]  
# Ожидаемый результат: 2 или 3 (зависит от реализации, т.к. словари нехэшируемы)

# 8. None и экземпляры классов
test8 = [None, None, object(), object()]  
# Ожидаемый результат: 3 (None + два разных объекта)

# 9. Вложенные структуры
test9 = [[1, [2]], [1, [2]], [1, [2, 3]]]  
# Ожидаемый результат: 2 (первые два списка идентичны, третий отличается)

